word = input("Введите слово из латинских букв: ")
length = len(word)

if length % 2 == 1:
    middle_index = length // 2
    print("Средняя буква:", word[middle_index])
else:
    middle_index1 = length // 2 - 1
    middle_index2 = length // 2
    print("Две средние буквы:", word[middle_index1] + word[middle_index2])